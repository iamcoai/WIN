import { betterAuth } from "better-auth";
import { drizzleAdapter } from "better-auth/adapters/drizzle";
import { nextCookies } from "better-auth/next-js";
import { db } from "./db";
import * as schema from "./db/schema";

export const auth = betterAuth({
  database: drizzleAdapter(db, {
    provider: "pg",
    schema,
  }),
  emailAndPassword: {
    enabled: true,
    minPasswordLength: 8,
  },
  session: {
    expiresIn: 60 * 60 * 24 * 7,
    updateAge: 60 * 60 * 24,
  },
  // Propagate Set-Cookie headers from server-actions to the response.
  // Required for `auth.api.signInEmail({ headers })` called inside
  // server actions to actually set the session cookie in the browser.
  plugins: [nextCookies()],
});

export type Auth = typeof auth;
